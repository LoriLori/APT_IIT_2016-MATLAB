 function f=f_fcn(x,h)
 Gb=298;
        Ieff=x(1);
        Gt=x(2);
        Rc=x(3);
        Rc_1=x(4);
        p1=x(5);
        p2=x(6);
        p4=x(7);
        taom=x(8);
        atao=exp(h/taom);
        f=[h*(-p2*Ieff)+Ieff;...
            h*(p1*(Gb-Gt)-p4*Ieff*Gt+Rc)+Gt;...
            2*Rc/atao-Rc_1/(atao^2);...
            Rc;...
            p1;...
            p2;...
            p4;...
            taom];
    end